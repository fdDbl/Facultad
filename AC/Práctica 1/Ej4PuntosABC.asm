; IMPRIMIR LETRAS DE LA 'A' A LA 'Z'

ORG 1000H
LETRA DB "A"

ORG 2000H
MOV BX, OFFSET LETRA
MOV AL, 1

IMPRIMIR: INT 7
CMP LETRA, 5AH
JZ FIN
INC LETRA
JMP IMPRIMIR

FIN: HLT
END

