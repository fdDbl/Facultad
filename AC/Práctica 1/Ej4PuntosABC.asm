; IMPRIMIR LETRAS DE LA 'A' A LA 'Z'

ORG 1000H
LETRA DB "A"

ORG 2000H
MOV BX, OFFSET LETRA
MOV AL, 1

IMPRIMIR: INT 7
INC LETRA
CMP LETRA, 5BH
JNZ IMPRIMIR

FIN: HLT
END

; IMPRIMIR NUMEROS DE 0 AL 9

ORG 1000H
PRIMER_VALOR DB 30h

ORG 2000H
MOV BX, OFFSET PRIMER_VALOR
MOV AL, 1
CMP BYTE PTR[BX], 3AH
JZ FIN
JNZ IMPRIMIR

IMPRIMIR:
INT 7
INC BYTE PTR[BX]
CMP BYTE PTR[BX], 3AH
JZ FIN
JNZ IMPRIMIR

FIN: HLT
END

