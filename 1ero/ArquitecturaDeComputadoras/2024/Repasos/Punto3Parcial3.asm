ORG 1000H
VECTOR DW -10,20,-50,40,-88,120
LONG_VECTOR DB 6
MSJ1 DB "GANAN LOS POSITIVOS"
MSJ2 DB "GANAN LOS NEGATIVOS"
POSS DB 0
NEGS DB 0

ORG 3000H
CONTAR_NEGATIVOS: MOV BX, SP
ADD BX, 2
MOV CL, [BX]
ADD BX, 2
MOV BX, [BX]
MOV AL, [BX]

LOOP: CALL ES_NEGATIVO
DEC CL
JNZ SEGUIR
  MOV AL, NEGS
  JMP FIN_CONTAR
SEGUIR: ADD BX, 2
MOV AL, [BX]
JMP LOOP

FIN_CONTAR:
RET

ORG 3100H
ES_NEGATIVO: AND AL, 10000000B
JNZ NEGATIVO
  MOV DX, 0
  INC POSS
  JMP FIN_ESNEG
NEGATIVO: MOV DX, 1
  INC NEGS
FIN_ESNEG: RET

ORG 2000H
MOV BX, OFFSET VECTOR
PUSH BX

MOV AL, LONG_VECTOR
PUSH AX

CALL CONTAR_NEGATIVOS

CMP AL, POSS
JS INFORMAR_GP
  MOV BX, OFFSET MSJ2
  MOV AL, OFFSET POSS - OFFSET MSJ2
  INT 7
  JMP FIN
INFORMAR_GP: MOV BX, OFFSET MSJ1
MOV AL, OFFSET MSJ2 - OFFSET MSJ1
INT 7
FIN: INT 0
END
